-- ~/.config/nvim/lua/settings.lua

-- Configuración básica de Neovim
vim.opt.number = true                   -- Mostrar números de línea
vim.opt.relativenumber = true           -- Números de línea relativos
vim.opt.expandtab = true                -- Convertir tabs a espacios
vim.opt.tabstop = 4                     -- Número de espacios en un tab
vim.opt.shiftwidth = 4                  -- Número de espacios para indentación
vim.opt.smartindent = true              -- Indentación inteligente
vim.opt.wrap = false                    -- No envolver líneas
vim.opt.swapfile = false                -- No crear archivos de swap
vim.opt.backup = false                  -- No crear archivos de respaldo
vim.opt.undofile = true                 -- Usar archivos de deshacer

